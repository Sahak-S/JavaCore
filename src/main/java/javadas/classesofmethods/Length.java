package javadas.classesofmethods;

public class Length {
    public static void main(String[] args) {


        int [] a1 = new int[10];
        int [] a2 = {1,2,3,5,-15,17,12,110};
        int [] a3 = {1,4,3,2};

        System.out.println("a1 =" + a1.length);
        System.out.println("a2 =" + a2.length);
        System.out.println("a3 =" + a3.length);
    }
}
